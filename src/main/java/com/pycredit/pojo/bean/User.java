package com.pycredit.pojo.bean;

public class User {
	int age;
	String id;
	String userName;
	String extraStr;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getExtraStr() {
		return extraStr;
	}
	
}
